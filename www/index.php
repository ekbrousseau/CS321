    <h2>Behind the Theme</h2>
    <p>Eating disorders can make it hard to ever feel comfortable in your body. Even in recovery, bad body image days can be really disheartening; having a changing body is difficult, and getting used to a new body takes a lot of time and patience. On the bad days, it can be helpful to remember that existing as you are is enough. Your physical appearance doesn't determine your worth in this world; your existence, exactly as it is right now, is enough.</p>
